class Assignment {
    public static void main(String args[]) {
        int a = 10; //assignment
        
        // a = a+10
        a += 10; //shorthand plus
        System.out.println(a);

        a = 10;
        // a = a-10
        a -= 10; //shorthand minus
        System.out.println(a);

        a = 10;
        // a = a*10
        a *= 10; //shorthand multiply
        System.out.println(a);

        a = 10;
        // a = a/10
        a /= 10; //shorthand divide
        System.out.println(a);
    }
}