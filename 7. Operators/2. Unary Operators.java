class Unary {
    public static void main(String args[]) {
        //pre increment
        int a = 10;
        int b = ++a;
        System.out.println(a);
        System.out.println(b+ "\n");

        //post increment
        int A = 10;
        int B = A++;
        System.out.println(A);
        System.out.println(B);
    }
}
