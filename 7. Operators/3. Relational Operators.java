class Relational {
    public static void main(String args[]) {
        int A = 10;
        int B = 5;
        int C = 10;

        //equal to
        System.out.println(A == B);
        System.out.println(A == C);
        System.out.println();

        //not equal to
        System.out.println(A != B);
        System.out.println(A != C);
        System.out.println();

        //greater
        System.out.println(A > B);
        System.out.println(B > A);
        System.out.println();

        //lesser
        System.out.println(A < B);
        System.out.println(B < A);
        System.out.println();

        //greater than equal to
        System.out.println(A >= B);
        System.out.println(A >= C);
        System.out.println();

        //lesser than equal to
        System.out.println(A <= B);
        System.out.println(A <= C);
        System.out.println();
    }    
}